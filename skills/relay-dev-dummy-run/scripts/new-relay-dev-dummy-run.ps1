function Save-DummyArtifact {
    param(
        [Parameter(Mandatory)]$ArtifactPaths,
        [Parameter(Mandatory)][string]$ProjectRoot,
        [Parameter(Mandatory)][string]$RunId,
        [Parameter(Mandatory)][ValidateSet("run", "task")][string]$Scope,
        [Parameter(Mandatory)][string]$Phase,
        [Parameter(Mandatory)][string]$ArtifactId,
        [Parameter(Mandatory)]$Content,
        [string]$TaskId
    )

    $path = Save-Artifact -ProjectRoot $ProjectRoot -RunId $RunId -Scope $Scope -Phase $Phase -ArtifactId $ArtifactId -Content $Content -TaskId $TaskId -AsJson
    $ArtifactPaths.Add([ordered]@{
        scope = $Scope
        phase = $Phase
        artifact_id = $ArtifactId
        task_id = if ([string]::IsNullOrWhiteSpace($TaskId)) { $null } else { $TaskId }
        path = $path
    }) | Out-Null
}

