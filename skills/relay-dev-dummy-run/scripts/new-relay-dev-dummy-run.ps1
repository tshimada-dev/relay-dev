[CmdletBinding()]
param(
    [string]$ProjectRoot = "",
    [ValidateSet("phase5-ready", "phase6-reject", "task-group", "ready-ui", "approval")]
    [string]$Scenario = "phase5-ready",
    [string]$RunId = "",
    [int]$TaskCount = 3,
    [switch]$Force
)

$ErrorActionPreference = "Stop"
Set-StrictMode -Version Latest

function Resolve-RelayDevProjectRoot {
    param([string]$StartPath)

    $candidate = if ([string]::IsNullOrWhiteSpace($StartPath)) { (Get-Location).Path } else { $StartPath }
    $candidate = [System.IO.Path]::GetFullPath($candidate)
    if (Test-Path -LiteralPath $candidate -PathType Leaf) {
        $candidate = Split-Path -Parent $candidate
    }

    while (-not [string]::IsNullOrWhiteSpace($candidate)) {
        if ((Test-Path -LiteralPath (Join-Path $candidate "app\core\run-state-store.ps1")) -and
            (Test-Path -LiteralPath (Join-Path $candidate "app\core\artifact-repository.ps1"))) {
            return $candidate
        }

        $parent = Split-Path -Parent $candidate
        if ($parent -eq $candidate) {
            break
        }
        $candidate = $parent
    }

    throw "Could not find a relay-dev project root from '$StartPath'. Pass -ProjectRoot explicitly."
}

function New-DummyTask {
    param(
        [Parameter(Mandatory)][string]$TaskId,
        [string]$ParallelSafety = "parallel",
        [string[]]$DependsOn = @()
    )

    $dependencyList = @(
        @($DependsOn) |
            ForEach-Object { [string]$_ } |
            Where-Object { -not [string]::IsNullOrWhiteSpace($_) }
    )

    return [ordered]@{
        task_id = $TaskId
        purpose = "Dummy task $TaskId for relay-dev verification"
        changed_files = @("src/$TaskId.txt")
        acceptance_criteria = @("Dummy scenario can be inspected safely")
        boundary_contract = [ordered]@{
            module_boundaries = @("dummy/$TaskId")
            public_interfaces = @("$TaskId public contract")
            allowed_dependencies = @("$TaskId -> local dummy dependency")
            forbidden_dependencies = @("$TaskId -> production side effect")
            side_effect_boundaries = @("$TaskId owns only dummy files")
            state_ownership = @("$TaskId owns dummy state")
        }
        visual_contract = [ordered]@{
            mode = "not_applicable"
            design_sources = @()
            visual_constraints = @()
            component_patterns = @()
            responsive_expectations = @()
            interaction_guidelines = @()
        }
        dependencies = @($dependencyList)
        depends_on = @($dependencyList)
        tests = @("pwsh -NoLogo -NoProfile -Command `"Write-Host dummy test for $TaskId`"")
        complexity = "small"
        resource_locks = @("file:src/$TaskId.txt")
        parallel_safety = $ParallelSafety
    }
}

function New-DummyTasks {
    param([int]$Count = 3)

    $tasks = New-Object System.Collections.Generic.List[object]
    for ($index = 1; $index -le [Math]::Max(1, $Count); $index++) {
        $taskId = "T-{0:D2}" -f $index
        $safety = "parallel"
        if ($index -eq 2) { $safety = "cautious" }
        if ($index -eq 3) { $safety = "serial" }
        $dependsOn = if ($index -eq 3) { @("T-01") } else { [string[]]@() }
        $tasks.Add((New-DummyTask -TaskId $taskId -ParallelSafety $safety -DependsOn $dependsOn)) | Out-Null
    }

    return @($tasks.ToArray())
}

